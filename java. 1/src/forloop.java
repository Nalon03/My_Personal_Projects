
public class forloop {
  
	public static void main(String[] args)
	{
	
		for(int i=20; i>1; i--){
        System.out.println("The value is: " +i);	
		
		}
		
		
		
		
		int[]array= {2,4,6,8,10};
		
		for(int a=0; a<array.length; a++)
		{
			
			System.out.println(array[a]);
		}
		
		
		}
		
	}

